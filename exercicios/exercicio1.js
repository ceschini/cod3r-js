// Crie uma função que dado dois valores(passados como parâmetros)
// mostre no console a soma, subtração, multiplicação e divisão desses
// valores

function operacoesBasicas(x, y) {
    console.log(`Soma de ${x} e ${y}: ${x + y}`);
    console.log(`Subtração de ${x} e ${y}: ${x - y}`);
    console.log(`Multiplicação de ${x} e ${y}: ${x * y}`);
    console.log(`Divisão de ${x} e ${y}: ${x / y}`);
}

operacoesBasicas(2, 3);